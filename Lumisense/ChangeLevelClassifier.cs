namespace AudioPlayer;

/// <summary>
/// Определяет "вес" одной строки изменения из changelog — <see cref="Level.Patch"/> (мелочь),
/// <see cref="Level.Minor"/> (крупная новая возможность) или <see cref="Level.Major"/>
/// (несовместимое изменение) — по СМЫСЛУ ТЕКСТА изменения, а не по его формальному типу
/// (added/changed/fixed/removed). Используется в ChangelogLoader для автоматического расчёта
/// версии changelog в стиле SemVer (см. ChangelogLoader.BumpForChanges).
///
/// Почему нельзя смотреть только на type: "added: Добавлена кнопка" — это Patch, а не Minor,
/// хотя тип у неё тот же "added", что и у "added: Добавлен полноэкранный режим" — а это уже
/// Minor. Раньше алгоритм ошибочно бампал minor на любом added/changed — здесь вместо этого
/// смотрим на СОДЕРЖАНИЕ текста:
///   - упоминание того, что старые данные/сохранённые настройки/прежний формат ломаются или
///     перестают работать, либо что архитектура сменилась целиком → Major;
///   - упоминание отдельной крупной сущности целиком (новый режим, новое окно, новый экран,
///     новая система/подсистема, крупный раздел настроек, поддержка новой технологии и т.п.),
///     которая появилась или была переработана → Minor;
///   - всё остальное (мелкие правки, отдельные кнопки/настройки/горячие клавиши, исправления
///     ошибок, изменения дизайна и логики без появления нового крупного раздела) → Patch.
///
/// Списки ключевых слов ниже — единственное, что нужно трогать, чтобы научить классификатор
/// новым формулировкам: сам алгоритм расчёта версии (ChangelogLoader) их не касается, поэтому
/// правила можно расширять без переписывания остального кода.
/// </summary>
public static class ChangeLevelClassifier
{
    public enum Level
    {
        Patch = 0,
        Minor = 1,
        Major = 2,
    }

    // ---------- Major: несовместимые изменения ----------
    // Что-то, из-за чего старые данные / сохранённые настройки / прежнее поведение перестают
    // работать: полная смена архитектуры, несовместимый формат данных, удаление целой системы
    // (а не отдельной функции внутри неё — просто "removed" сам по себе Major не даёт, см.
    // ChangelogLoader.BumpForChanges).
    private static readonly string[] MajorMarkers =
    {
        "смена архитектур",
        "переход на новую архитектур",
        "несовместим",
        "потеря совместимост",
        "старый формат данных",
        "прежний формат данных",
        "формат данных изменил",
        "формат данных изменен",
        "формат данных изменён",
        "старые данные",
        "больше не работ",
        "перестанут работать",
        "перестал работать",
        "удалена старая систем",
        "удалена старая архитектур",
        "полностью удалена систем",
        "breaking change",
    };

    // ---------- Minor: крупная новая возможность ----------
    // Конкретные формулировки прямо из ТЗ ("Добавлено окно настроек", "Добавлен мини-плеер",
    // "Добавлена поддержка SMTC" и т.д.) плюс родовые признаки той же по масштабу сущности.
    private static readonly string[] MinorFeatureMarkers =
    {
        "окно настроек",
        "мини-плеер",
        "миниплеер",
        "полноэкранн",
        "система патчноутов",
        "патчноут",
        "автосохранени",
        "система поиска",
        "новая архитектурная возможност",
        "архитектурная возможност",
        "раздел настроек",
        "функционал плейлист",
        "новую подсистем",
        "новая подсистем",
        "новое окно",
        "новый экран",
        "новый режим",
        "новая систем",
        "новую систем",
        "нового режима работы",
        "режим работы",
    };

    // "поддержка" сама по себе слишком общее слово (например "поддержка тем оформления" может
    // быть мелочью) — считаем это признаком крупного изменения только вместе с одним из этих
    // слов рядом (обычно так фразы вида "добавлена поддержка SMTC/новой технологии" и выглядят).
    private static readonly string[] TechnologySupportContext =
    {
        "smtc", "технологи", "формата", "протокол", "устройств", "windows",
    };

    // Родовые "крупные" существительные — используются вместе с одним из NewWordMarkers
    // ("новый"/"новая"/"новое"/"новую") для более общего правила, которое ловит формулировки,
    // прямо не перечисленные выше (например, "Добавлена новая большая система статистики").
    private static readonly string[] BigEntityNouns =
    {
        "режим", "окно", "экран", "систем", "подсистем", "раздел настроек", "архитектур",
    };

    private static readonly string[] NewWordMarkers = { "новый", "новая", "новое", "новую", "новых" };

    // Слова-маркеры удаления — используются вместе с BreakingRemovalNouns ниже, чтобы отличить
    // "удаление старой системы" (ТЗ прямо называет это примером Major) от обычного "Убрана
    // кнопка Y" (мелкая правка, Patch) — само по себе removed/"удал..." Major не даёт, см.
    // комментарий у ChangelogLoader.BumpForChanges.
    private static readonly string[] RemovalWordMarkers = { "удал", "убра", "снят" };

    // Только те сущности, снятие которых само по себе, по смыслу ТЗ, — несовместимое изменение
    // (в отличие от BigEntityNouns выше, который используется для признака КРУПНОЙ НОВОЙ
    // возможности, а не для признака поломки при удалении).
    private static readonly string[] BreakingRemovalNouns = { "систем", "подсистем", "архитектур", "формат данных" };

    public static Level Classify(ChangeItem change)
    {
        var text = (change.Text ?? "").ToLowerInvariant();
        if (text.Length == 0) return Level.Patch;

        if (ContainsAny(text, MajorMarkers))
            return Level.Major;

        if (ContainsAny(text, RemovalWordMarkers) && ContainsAny(text, BreakingRemovalNouns))
            return Level.Major;

        if (ContainsAny(text, MinorFeatureMarkers))
            return Level.Minor;

        if (text.Contains("поддержк") && ContainsAny(text, TechnologySupportContext))
            return Level.Minor;

        if (ContainsAny(text, NewWordMarkers) && ContainsAny(text, BigEntityNouns))
            return Level.Minor;

        return Level.Patch;
    }

    private static bool ContainsAny(string text, string[] markers)
    {
        foreach (var marker in markers)
        {
            if (text.Contains(marker))
                return true;
        }
        return false;
    }
}
